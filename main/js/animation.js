window.addEventListener('scroll', () =>{
    let konten = document.querySelector('.row');
    let posisiKonten = konten.getBoundingClientRect().top;
    let posisiLayar = window.innerHeight;
        if(posisiKonten < posisiLayar){
            konten.classList.add('active');
        }else{
            konten.classList.remove('active');
        }
});


